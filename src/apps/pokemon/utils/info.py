class PokemonInfo(object):
    def __init__(self, name: str, lv: int, hp: int, strength: int, defense: int, speed: int, tg: int, tf: int):
        self.name = name
        self.lv = lv
        self.hp = hp
        self.strength = strength
        self.defense = defense
        self.speed = speed
        self.tg = tg
        self.tf = tf
